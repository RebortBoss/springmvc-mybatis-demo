package com.tyxj.bean.dto;

public class DeviceStatusDTO {
	
	/**
	 * 设备状态名称
	 */
	private String key;
	
	/**
	 * 设备状态值
	 */
	private String value;

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	
	
	
}